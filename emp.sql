CREATE GENERATOR G_ERRORGWP;
SET GENERATOR G_ERRORGWP TO 0;

CREATE GENERATOR G_ERRORTWP;
SET GENERATOR G_ERRORTWP TO 0;

 -- ОБРАБОТКА ОШИБОК

 -- ГРУППА НЕ МОЖЕТ БЫТЬ НА НЕСКОЛЬКИХ ЗАНЯТИЯХ ОДНОВРЕМЕННО
SET TERM ^ ;
CREATE TRIGGER T_ERRORGWP FOR SCHEDULES
AFTER INSERT OR UPDATE OR DELETE
AS
DECLARE ID_NEW INTEGER;
BEGIN
	IF ((
		SELECT COUNT(*)
		FROM SCHEDULES
		WHERE NEW.GROUPID = SCHEDULES.GROUPID
			AND NEW.WEEKDAYID = SCHEDULES.WEEKDAYID
			AND NEW.PAIRID = SCHEDULES.PAIRID
		) = 1)
	THEN
		UPDATE SCHEDULES
		SET SCHEDULES.ERRORGWP = 0
		WHERE NEW.GROUPID = SCHEDULES.GROUPID
			AND NEW.WEEKDAYID = SCHEDULES.WEEKDAYID
			AND NEW.PAIRID = SCHEDULES.PAIRID
			AND NEW.ITEMID = SCHEDULES.ITEMID
			AND NEW.EDUCID = SCHEDULES.EDUCID
			AND NEW.TEACHERID = SCHEDULES.TEACHERID
			AND NEW.AUDIENCEID = SCHEDULES.AUDIENCEID
			AND NEW.ERRORGWP = SCHEDULES.ERRORGWP
			AND SCHEDULES.ERRORGWP > 0;

	IF ((
		SELECT COUNT(*)
		FROM SCHEDULES
		WHERE OLD.GROUPID = SCHEDULES.GROUPID
			AND OLD.WEEKDAYID = SCHEDULES.WEEKDAYID
			AND OLD.PAIRID = SCHEDULES.PAIRID
		) = 1)
	THEN
		UPDATE SCHEDULES
		SET SCHEDULES.ERRORGWP = 0
		WHERE OLD.GROUPID = SCHEDULES.GROUPID
			AND OLD.WEEKDAYID = SCHEDULES.WEEKDAYID
			AND OLD.PAIRID = SCHEDULES.PAIRID
			AND SCHEDULES.ERRORGWP > 0;

	ID_NEW = (SELECT SCHEDULES.ERRORGWP
		FROM SCHEDULES
		WHERE NEW.GROUPID = SCHEDULES.GROUPID
			AND NEW.WEEKDAYID = SCHEDULES.WEEKDAYID
			AND NEW.PAIRID = SCHEDULES.PAIRID
		ORDER BY SCHEDULES.ERRORGWP DESC
		ROWS 1);

	IF ((
		SELECT COUNT(*)
		FROM SCHEDULES
		WHERE NEW.GROUPID = SCHEDULES.GROUPID
			AND NEW.WEEKDAYID = SCHEDULES.WEEKDAYID
			AND NEW.PAIRID = SCHEDULES.PAIRID
	) > 1)
	THEN
		IF ((ID_NEW = 0)
			OR (NEW.ERRORGWP = OLD.ERRORGWP) AND (NEW.ERRORTWP = OLD.ERRORTWP))
		THEN
			ID_NEW = GEN_ID(G_ERRORGWP, 1);

		UPDATE SCHEDULES
		SET SCHEDULES.ERRORGWP = :ID_NEW
		WHERE NEW.GROUPID = SCHEDULES.GROUPID
			AND NEW.WEEKDAYID = SCHEDULES.WEEKDAYID
			AND NEW.PAIRID = SCHEDULES.PAIRID
			AND SCHEDULES.ERRORGWP <> :ID_NEW;
END^
SET TERM ; ^

 -- ПРЕПОДАВАТЕЛЬ НЕ МОЖЕТ БЫТЬ НА НЕСКОЛЬКИХ ЗАНЯТИЯХ ОДНОВРЕМЕННО
